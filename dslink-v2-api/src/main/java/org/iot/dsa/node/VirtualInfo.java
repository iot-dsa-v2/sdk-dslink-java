package org.iot.dsa.node;

/**
 * Used to create temporary info's for virtual actions in DSNode.
 */
class VirtualInfo extends DSInfo {

    ///////////////////////////////////////////////////////////////////////////
    // Constructors
    ///////////////////////////////////////////////////////////////////////////

    public VirtualInfo(String name, DSIObject obj) {
        super(name, obj);
    }

    ///////////////////////////////////////////////////////////////////////////
    // Package / Private Methods
    ///////////////////////////////////////////////////////////////////////////

    @Override
    boolean isVirtual() {
        return true;
    }

}
